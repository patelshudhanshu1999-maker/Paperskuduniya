                                <div class="news-block-body">
                                    <p>Tata Consultancy Services (TCS), India’s largest IT services firm, is set to fire nearly 12,000 employees - around 2% of its global workforce - in what is being described as one of the most significant workforce restructurings in the company's history. Contrary to industry speculation, the layoffs are not driven by artificial intelligence (AI)-induced automation or productivity gains, the tech giant said. </p>
                                    <p>TCS CEO and MD K Krithivasan, in an interview with Moneycontrol, firmly dispelled the notion that the job cuts are a result of AI replacing human workers. “This is not because of AI giving some 20% productivity gains. We are not doing that,” Krithivasan said. “This is driven by where there is a skill mismatch or where we think we have not been able to deploy someone.”</p>

                                    <h5>Why the layoffs in TCS?</h5>
                                    <p>The layoffs, which will take place gradually over the course of FY26, will primarily affect mid- to senior-level employees. A portion of junior staff who have remained on extended bench time - those not currently deployed on any project - will also be impacted.</p>
                                    <p>Despite TCS's massive upskilling initiatives, having trained over 550,000 employees in basic AI skills and over 100,000 in advanced capabilities, the company has acknowledged challenges in redeploying trained staff, particularly senior professionals. “Some people, especially at senior levels, find it difficult to transition to tech-heavy roles,” Krithivasan noted.</p>
                                    <p>This disconnect between training and redeployment has been a core driver of the layoffs.</p>

                                    <h5>Structural and strategic shifts</h5>
                                    <p>Adding to the pressure is a fundamental change in TCS’s operational structure. The company is shifting away from traditional “waterfall” project management methods toward an agile, product-centric delivery model. This transition reduces the need for conventional project and program managers, particularly those managing multiple leadership layers.</p>
                                    <p>“Earlier, in waterfall models, we had multiple leadership layers. That’s changing,” Krithivasan explained. The structural shift is designed to make the company more responsive and efficient in an increasingly competitive tech landscape.</p>
                                    <p>Acknowledging the sensitivity of the move, Krithivasan described the decision as “difficult but necessary,” assuring that affected employees would be treated with care and dignity. TCS has promised a comprehensive exit package including notice period pay, severance benefits, extended health insurance, mental health counseling, and outplacement assistance.</p>
                                    <p>The layoffs are not targeted by geography or business domain and will roll out over the next three quarters of FY26.</p>
