                  <h4>Indian Union Territories and Capitals</h4>
                  <p>At present, <strong>India has 8 Union Territories</strong>. One of the most significant changes in recent years was the <strong>reorganization of the erstwhile state of Jammu and Kashmir</strong>. On <strong>5–6 August 2019</strong>, the Indian Parliament passed the Jammu and Kashmir Reorganisation Act, under which <strong>Article 370 and Article 35A</strong> were abrogated. This historic step removed the special status of Jammu and Kashmir.</p>
                  <p>Following this, the state was <strong>bifurcated into two Union Territories — Jammu & Kashmir and Ladakh —</strong> with effect from 31 October 2019. This was the first instance of a state being reorganized and downgraded into Union Territories.</p>
                  <p>With this change, India officially comprises <strong>28 States and 8 Union Territories</strong> today. Each Union Territory is administered differently, with some having their own legislatures and others being directly governed by the <strong>Central Government</strong> through an appointed Lieutenant Governor or Administrator.</p>
